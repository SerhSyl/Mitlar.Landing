-- Створення таблиці для динамічного контенту сайту (Банери, Секція "Про гру")
CREATE TABLE site_settings (
  id SERIAL PRIMARY KEY,
  section TEXT NOT NULL,         -- Назва секції (напр. 'hero', 'about_game')
  block_key TEXT UNIQUE NOT NULL,-- Унікальний ключ блоку (напр. 'main_banner', 'zone_1')
  title TEXT,                    -- Заголовок блоку
  content TEXT,                  -- Основний текст / опис
  image_url TEXT,                -- Посилання на картинку (якщо є)
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

